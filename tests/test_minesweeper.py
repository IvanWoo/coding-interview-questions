from puzzles.minesweeper import update_board


def test_update_board():
    assert update_board(
        [
            ["E", "E", "E", "E", "E"],
            ["E", "E", "M", "E", "E"],
            ["E", "E", "E", "E", "E"],
            ["E", "E", "E", "E", "E"],
        ],
        [3, 0],
    ) == [
        ["B", "1", "E", "1", "B"],
        ["B", "1", "M", "1", "B"],
        ["B", "1", "1", "1", "B"],
        ["B", "B", "B", "B", "B"],
    ]

    assert update_board(
        [
            ["B", "1", "E", "1", "B"],
            ["B", "1", "M", "1", "B"],
            ["B", "1", "1", "1", "B"],
            ["B", "B", "B", "B", "B"],
        ],
        [1, 2],
    ) == [
        ["B", "1", "E", "1", "B"],
        ["B", "1", "X", "1", "B"],
        ["B", "1", "1", "1", "B"],
        ["B", "B", "B", "B", "B"],
    ]
    assert update_board(
        [
            ["E", "E", "E", "E", "E", "E", "E", "E"],
            ["E", "E", "E", "E", "E", "E", "E", "M"],
            ["E", "E", "M", "E", "E", "E", "E", "E"],
            ["M", "E", "E", "E", "E", "E", "E", "E"],
            ["E", "E", "E", "E", "E", "E", "E", "E"],
            ["E", "E", "E", "E", "E", "E", "E", "E"],
            ["E", "E", "E", "E", "E", "E", "E", "E"],
            ["E", "E", "M", "M", "E", "E", "E", "E"],
        ],
        [0, 0],
    ) == [
        ["B", "B", "B", "B", "B", "B", "1", "E"],
        ["B", "1", "1", "1", "B", "B", "1", "M"],
        ["1", "2", "M", "1", "B", "B", "1", "1"],
        ["M", "2", "1", "1", "B", "B", "B", "B"],
        ["1", "1", "B", "B", "B", "B", "B", "B"],
        ["B", "B", "B", "B", "B", "B", "B", "B"],
        ["B", "1", "2", "2", "1", "B", "B", "B"],
        ["B", "1", "M", "M", "1", "B", "B", "B"],
    ]
